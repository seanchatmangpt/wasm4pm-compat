/// Compile-time proof that every entry in [`ALL_WITNESS_KEYS`] is unique.
///
/// If two witnesses ever collide on `KEY`, this `const` block fails to evaluate
/// with `duplicate witness key — authority collision`, breaking the build.
const _: () = {
    let keys = ALL_WITNESS_KEYS;
    let n = keys.len();
    let mut i = 0;
    while i < n {
        let mut j = i + 1;
        while j < n {
            assert!(
                !crate::witness_law::str_eq(keys[i], keys[j]),
                "duplicate witness key — authority collision in ALL_WITNESS_KEYS"
            );
            j += 1;
        }
        i += 1;
    }
};